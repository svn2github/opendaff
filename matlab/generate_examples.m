metadata = daff_add_metadata( metadata, 'Generation script', 'String', 'Opendaff-v1.7/matlab/generate_examples.m' );

addpath( 'datafuncs' )

daff_write('filename', 'ExampleUnityOmni.ms.daff', ...
           'content', 'ms', ...
           'datafunc', @dfUnityMS, ...
           'channels', channels, ...
           'alphares', alpha_res, ...
           'betares', beta_res, ...
           'orient', default_orient, ...
           'metadata', metadata);
